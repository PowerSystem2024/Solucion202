# Operadores Aritméticos: +, -, *, /, //, %, **

# Ejemplos:
a = 10
b = 3
suma = a + b  # Suma
resta = a - b  # Resta
multiplicacion = a * b  # Multiplicación
division = a / b  # División
division_entera = a // b  # División entera
modulo = a % b  # Módulo (resto de la división)
potencia = a ** b  # Potencia

# Operadores de Comparación: ==, !=, <, >, <=, >=

# Ejemplos:
x = 5
y = 10
igual = x == y  # Igualdad
distinto = x != y  # Distinto
menor_que = x < y  # Menor que
mayor_que = x > y  # Mayor que
menor_o_igual = x <= y  # Menor o igual que
mayor_o_igual = x >= y  # Mayor o igual que

# Operadores Lógicos: and, or, not

# Ejemplos:
p = True
q = False

# AND lógico (ambas condiciones deben ser verdaderas para que sea verdadero)
and_resultado = p and q

# OR lógico (al menos una de las condiciones debe ser verdadera para que sea verdadero)
or_resultado = p or q

# NOT lógico (invierte el valor de verdad de la condición)
not_resultado = not p

# Puedes usar paréntesis para agrupar expresiones lógicas:
complejo = (p and q) or (not p)

# Operadores de Pertenencia: in, not in

# Ejemplos:
lista = [1, 2, 3, 4, 5]
pertenece = 3 in lista  # True si 3 está en la lista
no_pertenece = 6 not in lista  # True si 6 no está en la lista

# Operadores de Identidad: is, is not

# Ejemplos:
x = [1, 2, 3]
y = [1, 2, 3]
es_mismo_objeto = x is y  # True si x y y apuntan al mismo objeto en memoria
no_es_mismo_objeto = x is not y  # True si x y y no apuntan al mismo objeto en memoria



