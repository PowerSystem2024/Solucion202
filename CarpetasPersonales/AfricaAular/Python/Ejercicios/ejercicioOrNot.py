'''
Ejercicio operador or/not
La pregunta es si un padre puede asistir al juego de su hijo
1.Verificamos si tiene vacaciones
2. Verificamos si tiene el dia libre
3. Usar estructura if else con el operador or
4. imprimir
'''

#or
vacaciones = True
libre = False
if vacaciones or libre:{
  print("Puede asistir")
}
else:{
  print("No puede asistir")
}
  
#not
vacaciones = True
libre = False
if not vacaciones or libre:{
  print("Puede asistir")
}
else:{
  print("No puede asistir")
}
  
